    private void btnFlightNumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFlightNumActionPerformed
        // TODO add your handling code here:
        String flightNum = tfFlightNum.getText();
        ArrayList<Customer> result = cusDir.searchByFlightNum(flightNum);
        if(result.size() == 0 ){
            JOptionPane.showMessageDialog(null, "Flight number you entered does not exist","Information",JOptionPane.INFORMATION_MESSAGE);
        }else{
            populateTable(result);
        }
    }//GEN-LAST:event_btnFlightNumActionPerformed

    public void populateTable(ArrayList<Customer> result){
        DefaultTableModel dtm = (DefaultTableModel) tblReservation.getModel();
        dtm.setRowCount(0);
        for(Customer cus : result){
            Object[] row = new Object[5];
            row[0] = cus;
            row[1] = cus.getAge();
            row[2] = cus.getIdNumber();
            row[3] = cus.getSeatId();
            row[4] = cus.getSeat();
            dtm.addRow(row);
        }
        
    }
    void populateCu(){
        
        DefaultTableModel dtm = (DefaultTableModel) tblReservation.getModel();
        dtm.setRowCount(0);
        for(Customer cus : cusDir.getCustomerList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = cus;
            row[1] = cus.getAge();
            row[2] = cus.getIdNumber();
            row[3] = cus.getSeatId();
            row[4] = cus.getSeat();
            dtm.addRow(row);
        }
    }

