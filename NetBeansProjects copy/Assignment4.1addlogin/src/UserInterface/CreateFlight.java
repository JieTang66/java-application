    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")||jTextField4.getText().equals("")
                ||jTextField5.getText().equals("")||jTextField6.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please type in the all the txtField");
            return;       
        }else{
            Flight flight = flsch.addFlight();
            flight.setFlightNumber(jTextField1.getText());
            flight.setTimeStart(jTextField2.getText());
            flight.setTimeofday(jTextField3.getText());
            flight.setTimeEnd(jTextField4.getText());
            flight.setDepartureLocation(jTextField5.getText());
            flight.setArrivalLocation(jTextField6.getText());
            try {
                flight.setDate(jTextField7.getText());
            } catch (ParseException ex) {
           // Logger.getLogger(CreateFlight.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Please type in a right Date");
                return;
            } 
            flight.setAirliner(comboAirliner.getItemAt(comboAirliner.getSelectedIndex()));
        //flight.setSeatNumber(Integer.valueOf(String.valueOf(flsch.flightList.get(Integer.valueOf(comboAirliner.getSelectedIndex())))));
        //jTextField8.setText(String.valueOf(flsch.flightList.get(Integer.valueOf(comboAirliner.getSelectedIndex()))));
            JOptionPane.showMessageDialog(null, "Account created successfully");
            toMainScreen();
        }
        
        
        
        
        
    }//GEN-LAST:event_btnCreateActionPerformed
    private void toMainScreen(){
        CardLayout layout = (CardLayout) this.rightPanel.getLayout();
        this.rightPanel.remove(this);
        Component[] comps = this.rightPanel.getComponents();
        for(Component comp:comps){
            if(comp instanceof FlightJPanel){
                FlightJPanel panel = (FlightJPanel) comp;
                panel.populate();
            }
        }
        layout.previous(rightPanel);
    }

