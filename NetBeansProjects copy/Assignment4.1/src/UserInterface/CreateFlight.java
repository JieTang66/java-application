    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        Flight flight = flsch.addFlight();
        flight.setFlightNumber(jTextField1.getText());
        flight.setTimeStart(jTextField2.getText());
        flight.setTimeofday(jTextField3.getText());
        flight.setTimeEnd(jTextField4.getText());
        flight.setDepartureLocation(jTextField5.getText());
        flight.setArrivalLocation(jTextField6.getText());
        flight.setDate(jTextField7.getText());
        flight.setAirliner(jComboBox1.getItemAt(jComboBox1.getSelectedIndex()));
        JOptionPane.showMessageDialog(null, "Account created successfully");
        toMainScreen();
    }//GEN-LAST:event_btnCreateActionPerformed
    private void toMainScreen(){
        CardLayout layout = (CardLayout) this.rightPanel.getLayout();
        this.rightPanel.remove(this);
        Component[] comps = this.rightPanel.getComponents();
        for(Component comp:comps){
            if(comp instanceof FlightJPanel){
                FlightJPanel panel = (FlightJPanel) comp;
                panel.populate();
            }
        }
        layout.previous(rightPanel);
    }

