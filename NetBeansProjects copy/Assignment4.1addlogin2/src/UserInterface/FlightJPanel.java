    void populate(){    
        DefaultTableModel dtm = (DefaultTableModel) tblAccounts.getModel();
        dtm.setRowCount(0);
        for(Flight f : flsch.getFlightList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=f.getAirliner();
            row[1]=f;
            row[2]=f.getTimeStart();
            row[3]=f.getTimeEnd();
            row[4]=f.getTimeofday();
            row[5]=f.getDepartureLocation();
            row[6]=f.getArrivalLocation();
            row[7]=f.getDate();
            row[8]=f.getSeatNumber();
            dtm.addRow(row);
        }
    }
