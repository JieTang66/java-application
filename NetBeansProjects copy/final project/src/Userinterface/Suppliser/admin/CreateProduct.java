    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
         if(txtName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Name cannotbe empty");
            return;
        }else if(txtSalePrice.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "PurchasingPrice cannotbe empty");
            return;
        }else if(txtSalePrice.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Sale Price cannotbe empty");
            return;
        }else if(txtAvaliNum.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "AvaliNum cannotbe empty");
            return;
        }
        else if(typeCombo.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Please select type");
            return;
        }
         for(Product p:enterprise.getProductDir().getProductList()){
             if(p.getProductName().equals(txtName.getText())){
                 JOptionPane.showMessageDialog(null, "Name repeat!");
                 return;
             }
         }
        
        Network network = searchNetwork();
        
        Product product = enterprise.getProductDir().addProduct();
         int count=0;
                    for(Product p:enterprise.getProductDir().getProductList()){
                    count++;
                    }
            
                     product.setCount(count);
                     product.setProductId(product.getCount());
        
        String name = txtName.getText(); 
        String purchasingprice = txtSalePrice.getText();
        String saleprice = txtSalePrice.getText();
        String availNum = txtAvaliNum.getText();
        String typename = String.valueOf(typeCombo.getSelectedItem());
        
        product.setType(typename);
        product.setProductName(name);
        product.setPurchasingPrice(Double.parseDouble(purchasingprice));
        product.setSalesPrice(Double.parseDouble(saleprice));
        product.setAvailNum(Integer.parseInt(availNum));
        
        if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Face")){
            product.setFaceType((faceType)typeCombo.getSelectedItem());
        }else if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Lip")){
            product.setLipType((lipType)typeCombo.getSelectedItem());
        }else if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Eye")){
             product.setEyeType((eyeType)typeCombo.getSelectedItem());
        }
        
        JOptionPane.showMessageDialog(null, "Product Created Successfully");  
    }//GEN-LAST:event_btnCreateActionPerformed

        public Network searchNetwork(){
            for(Network network: business.getNetworkList()){
                if(network.getEnterpriseDirectory().getEnterpriseList().contains(enterprise))
                    return network;
        }
            return null;
        }
