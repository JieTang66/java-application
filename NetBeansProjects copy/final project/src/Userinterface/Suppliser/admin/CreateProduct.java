    public void poputypeCombo(){
        typeCombo.removeAllItems();
        
        
        if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Face")){
            for (Product.faceType facetype : Product.faceType.values()){
                typeCombo.addItem(facetype);
        }
        }else if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Lip")){
            for (Product.lipType liptype : Product.lipType.values()){
                typeCombo.addItem(liptype);
            }
        }else if(String.valueOf(useCombo.getSelectedItem()).equalsIgnoreCase("Eye")){
            for (Product.eyeType eyetype : Product.eyeType.values()){
                typeCombo.addItem(eyetype);
            }  
        }   
    }
        

    

