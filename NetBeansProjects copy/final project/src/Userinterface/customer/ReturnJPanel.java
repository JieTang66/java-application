    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 if(tnTxt.getText().isEmpty()||labelTxt.getText().isEmpty()||reasonTxt.getText().isEmpty()){

            JOptionPane.showMessageDialog(null, "Please add all the mendatory fields!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }      
         int selectedrow = orderTbl.getSelectedRow();
        if(selectedrow<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!labelTxt.getText().equals("RT20181211")){
            JOptionPane.showMessageDialog(null, "The lable is invaild", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }else{

        Order order = (Order)orderTbl.getValueAt(selectedrow, 0);
        order.setTrackingNum(tnTxt.getText());
        order.setLabel(labelTxt.getText());
        order.setReason(reasonTxt.getText());
        order.setStatues("Returned");
        
        int id = order.getOrderId();
         
        for(WorkRequest request : account.getWorkQueue().getWorkRequestList()){ 
            if(request instanceof OrderReminderRequest ){
            if(((OrderReminderRequest)request).getOrderId()==id){
            ((OrderReminderRequest)request).setReturnStatus("Returned");
             ((OrderReminderRequest)request).setStatus("Returned");
            
         //   ((OrderReminderRequest)request).setReturnMessage("Please agree!!");           
            }  
        }  
        }        
    }
        populateOrderTable();
        
         JOptionPane.showMessageDialog(null, "Returned Successfully,Money will be returned to your bank account three to five business days after we receive and check the products.");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

