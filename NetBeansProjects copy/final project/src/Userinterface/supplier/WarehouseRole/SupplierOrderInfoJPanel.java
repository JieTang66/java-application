    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = orderInfoTable.getSelectedRow();
         
        if(selectedRow<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        ProductRequest request = (ProductRequest)orderInfoTable.getValueAt(selectedRow, 0);
        if(request.getStatus().equals("shipped")) {
            JOptionPane.showMessageDialog(null, "Already Shipped", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }else {
           
        for(Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()){
            if(e instanceof BeautyEnterprise && e.getName().equals(request.getBeautyenterprisename())){
                for(Product p : senterprise.getProductDir().getProductList()){
                    if(p.getProductName().equals(request.getName())){
                        if(p.getAvailNum()<request.getQuality()){
                            JOptionPane.showMessageDialog(null, "Not Enough Inventory", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
                        }
                        int sum = p.getAvailNum() + request.getQuality();
                        p.setAvailNum(sum);
                    }
                }
            }
        }
        
        
        String name = request.getName();
        for(Product product : senterprise.getProductDir().getProductList()){
                if(product.getProductName().equals(name)){
                    int avail = product.getAvailNum()-request.getQuality();
               product.setAvailNum(avail);
            }
        }
        
        
        
        request.setStatus("shipped");
        request.setSupplierenterpirsename(senterprise.getName());
        }   
        populateorderInfoTable();
    }//GEN-LAST:event_processBtnActionPerformed

    private void viewDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailActionPerformed
        // TODO add your handling code here:
        int selectedrow = orderInfoTable.getSelectedRow();
        ProductRequest request = (ProductRequest)orderInfoTable.getValueAt(selectedrow, 0);
        String name = request.getName();
        for(Product product : senterprise.getProductDir().getProductList()){
            
                if(product.getProductName().equals(name)){
                nameTxt.setText(product.getProductName());
                productIdTxt.setText(Integer.toString(product.getProductId()));
                purchasePTxt.setText(Double.toString(product.getPurchasingPrice()));
                salePTxt.setText(Double.toString(product.getSalesPrice())); 
                quantityTxt.setText(Integer.toString(product.getAvailNum()));
            }
        }
    }//GEN-LAST:event_viewDetailActionPerformed
     
