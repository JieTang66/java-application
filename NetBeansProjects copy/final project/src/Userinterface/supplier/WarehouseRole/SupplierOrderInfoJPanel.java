    public void populateorderInfoTable(){
        DefaultTableModel model = (DefaultTableModel) orderInfoTable.getModel();
        
        model.setRowCount(0);
                for(WorkRequest request: senterprise.getWorkQueue().getWorkRequestList()){
                    if(request instanceof ProductRequest){                  
                        Object[] row = new Object[4];
                        row[0] = ((ProductRequest)request);
                        row[1] = ((ProductRequest)request).getQuality();
                        row[2] = ((ProductRequest)request).getBeautyenterprisename();
                        row[3] = request.getStatus();
                        model.addRow(row);
                    }
                } 
    }        
